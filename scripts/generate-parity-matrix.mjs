import { access, readFile, writeFile } from 'node:fs/promises';

const inventoryUrl = new URL('../docs/parity-inventory.json', import.meta.url);
const outputUrl = new URL('../docs/PARITY_MATRIX.md', import.meta.url);
const inventory = JSON.parse(await readFile(inventoryUrl, 'utf8'));

const evidence = {
  1: entry('src/core', 'tests/unit/core-contracts.test.ts'),
  2: entry('src/providers', 'tests/unit/provider-contracts.test.ts'),
  3: entry('src/providers', 'tests/golden/providers.test.ts'),
  4: entry('src/runtime/agent-loop.ts', 'tests/unit/agent-loop.test.ts'),
  5: entry('src/persistence', 'tests/unit/persistence-observability.test.ts'),
  6: entry(
    'src/workspaces + src/mcp',
    'tests/unit/workspaces.test.ts + tests/unit/mcp.test.ts',
    ['src/workspaces', 'src/mcp', 'tests/unit/workspaces.test.ts', 'tests/unit/mcp.test.ts'],
  ),
  7: entry('src/runtime/agent-sessions.ts', 'tests/unit/agent-sessions.test.ts'),
  8: entry('src/workspace-agents + src/skills', 'tests/unit/workspace-agents.test.ts', [
    'src/workspace-agents',
    'src/skills',
    'tests/unit/workspace-agents.test.ts',
  ]),
  9: entry(
    'src/pricing + src/cache + src/planning + src/config',
    'tests/unit/planning-accounting-config.test.ts',
    [
      'src/pricing',
      'src/cache',
      'src/planning',
      'src/config',
      'tests/unit/planning-accounting-config.test.ts',
    ],
  ),
  10: entry('src/realtime + src/providers/duplex-realtime.ts', 'tests/unit/realtime.test.ts', [
    'src/realtime',
    'src/providers/duplex-realtime.ts',
    'tests/unit/realtime.test.ts',
  ]),
  11: entry(
    'src/workers + src/observability',
    'tests/unit/workers-observability.test.ts',
    ['src/workers', 'src/observability', 'tests/unit/workers-observability.test.ts'],
  ),
};

const releaseEvidence = [
  'tests/journey/runtime-journey.test.ts',
  'tests/security/boundaries.test.ts',
  'tests/perf/runtime-budget.test.ts',
  'docs/CAPABILITIES.md',
  'docs/public-api.json',
  'docs/catalog-snapshot.json',
  'scripts/package-smoke.mjs',
];
await Promise.all(
  [...new Set([...Object.values(evidence).flatMap((value) => value.paths), ...releaseEvidence])].map(
    (path) => access(new URL(`../${path}`, import.meta.url)),
  ),
);

const rows = inventory.groups.flatMap((group) =>
  group.features.map((feature) => ({
    feature,
    domain: group.domain,
    phase: group.owner_phase,
    parent: group.parent_status,
    target: group.target_status,
    source: group.source,
  })),
);

const lines = [
  '# Python/TypeScript Parity Matrix',
  '',
  `Generated from \`docs/parity-inventory.json\` against Python commit \`${inventory.parent_commit}\`. Do not edit this file by hand; run \`pnpm generate:parity\`.`,
  '',
  `Coverage: **${rows.length} tracked requirements** (${inventory.catalog_unique_feature_count} unique FEATURES.md items plus implementation supplements).`,
  '',
  '| Requirement | Domain | Phase | Python status | TypeScript target | Implementation evidence | Test evidence | Baseline |',
  '| --- | --- | ---: | --- | --- | --- | --- | --- |',
];

for (const row of rows) {
  const { implementation, tests } = evidence[row.phase] ?? {
    implementation: 'unmapped',
    tests: 'unmapped',
  };
  lines.push(
    `| ${escapeCell(row.feature)} | ${escapeCell(row.domain)} | ${row.phase} | ${escapeCell(row.parent)} | ${escapeCell(row.target)} | \`${implementation}\` | \`${tests}\` | ${escapeCell(row.source)} |`,
  );
}
lines.push(
  '',
  '## Release evidence',
  '',
  ...releaseEvidence.map((path) => `- \`${path}\``),
  '',
);

const rendered = `${lines.join('\n')}\n`;
if (process.argv.includes('--check')) {
  const current = await readFile(outputUrl, 'utf8').catch(() => '');
  if (current !== rendered) {
    throw new Error('docs/PARITY_MATRIX.md is stale. Run pnpm generate:parity.');
  }
  console.log(`Parity matrix OK: ${rows.length} requirements have implementation and test evidence.`);
} else {
  await writeFile(outputUrl, rendered, 'utf8');
  console.log(`Wrote parity matrix with ${rows.length} requirements.`);
}

function escapeCell(value) {
  return String(value).replaceAll('|', '\\|').replaceAll('\n', ' ');
}

function entry(implementation, tests, paths = [implementation, tests]) {
  return { implementation, tests, paths };
}
