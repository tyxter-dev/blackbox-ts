import { access, readFile, writeFile } from 'node:fs/promises';

import { formatGenerated } from './lib/format-generated.mjs';

const inventoryUrl = new URL('../docs/parity-inventory.json', import.meta.url);
const outputUrl = new URL('../docs/PARITY_MATRIX.md', import.meta.url);
const inventory = JSON.parse(await readFile(inventoryUrl, 'utf8'));
const rows = inventory.groups.flatMap((group) =>
  group.features.map((feature) => ({ feature, group })),
);
const parentRows = rows.filter(({ group }) => group.classification === 'parent');
const supplements = rows.filter(({ group }) => group.classification === 'supplement');

const resolvedRows = rows.map(({ feature, group }) => ({
  feature,
  group,
  evidence: feature.evidence.map((ref) => inventory.evidence[ref]),
}));
await Promise.all(
  [
    ...new Set(
      resolvedRows.flatMap(({ evidence }) =>
        evidence.flatMap((entry) => [
          ...entry.typescript.sources,
          ...entry.typescript.tests,
        ]),
      ),
    ),
  ].map((path) => access(new URL(`../${path}`, import.meta.url))),
);

const lines = [
  '# Python/TypeScript Parity Matrix',
  '',
  `Generated from \`docs/parity-inventory.json\` schema v2 against Python commit \`${inventory.parent.commit}\`. Do not edit this file by hand; run \`pnpm generate:parity\`.`,
  '',
  `Parent parity: **${parentRows.length} catalog features**. Verification supplements: **${supplements.length}**. TypeScript extensions: **${inventory.extensions.length}**, excluded from the parent score.`,
  '',
  '| Stable ID | Requirement | Class | Domain | Phase | Python status | TypeScript target | Python evidence | TypeScript implementation | TypeScript tests |',
  '| --- | --- | --- | --- | ---: | --- | --- | --- | --- | --- |',
];

for (const row of resolvedRows) {
  lines.push(
    `| \`${escapeCell(row.feature.id)}\` | ${escapeCell(row.feature.name)} | ${row.group.classification} | ${escapeCell(row.group.domain)} | ${row.group.owner_phase} | ${escapeCell(row.group.parent_status)} | ${escapeCell(row.group.target_status)} | ${renderParentEvidence(row.evidence)} | ${renderTsLinks(unique(row.evidence.flatMap((entry) => entry.typescript.sources)))} | ${renderTsLinks(unique(row.evidence.flatMap((entry) => entry.typescript.tests)))} |`,
  );
}

lines.push('', '## TypeScript extensions excluded from parity', '');
for (const extension of inventory.extensions) {
  const records = extension.evidence.map((ref) => inventory.evidence[ref]);
  lines.push(
    `- \`${extension.id}\`: ${extension.name}. ${extension.rationale} Evidence: ${renderTsLinks(unique(records.flatMap((entry) => [...entry.typescript.sources, ...entry.typescript.tests])))}.`,
  );
}
lines.push(
  '',
  '## Release evidence',
  '',
  '- `tests/journey/runtime-journey.test.ts`',
  '- `tests/security/boundaries.test.ts`',
  '- `tests/perf/runtime-budget.test.ts`',
  '- `tests/golden/core-contracts.test.ts`',
  '- `tests/golden/python-provider-differential.test.ts`',
  '- `tests/golden/python-catalog-differential.test.ts`',
  '- `tests/golden/providers.test.ts`',
  '- `docs/parent-baseline.json`',
  '- `docs/parity-test-crosswalk.json`',
  '- `docs/PARITY_MAINTENANCE.md`',
  '- `docs/public-api.json`',
  '- `docs/catalog-snapshot.json`',
  '- `scripts/package-smoke.mjs`',
);

const rendered = await formatGenerated(`${lines.join('\n')}\n`, outputUrl);
if (process.argv.includes('--check')) {
  const current = await readFile(outputUrl, 'utf8').catch(() => '');
  if (current !== rendered) throw new Error('docs/PARITY_MATRIX.md is stale. Run pnpm generate:parity.');
  console.log(`Parity matrix OK: ${parentRows.length} parent features + ${supplements.length} supplements.`);
} else {
  await writeFile(outputUrl, rendered, 'utf8');
  console.log(`Wrote parity matrix: ${parentRows.length} parent features + ${supplements.length} supplements.`);
}

function renderParentEvidence(records) {
  const paths = unique(records.flatMap((entry) => [...entry.parent.sources, ...entry.parent.tests]));
  return paths
    .map((path) => {
      const url = `https://github.com/${inventory.parent.repository}/blob/${inventory.parent.commit}/${path}`;
      return `[\`${escapeCell(path)}\`](${url})`;
    })
    .join('<br>');
}

function renderTsLinks(paths) {
  return paths.map((path) => `[\`${escapeCell(path)}\`](../${path.replaceAll(' ', '%20')})`).join('<br>');
}

function escapeCell(value) {
  return String(value).replaceAll('|', '\\|').replaceAll('\n', ' ');
}

function unique(values) {
  return [...new Set(values)];
}
